#!/bin/bash

# ========================================================
# 限位挡块资产登记系统 - 停止脚本
# ========================================================

set -e

# 颜色定义
RED='\033[0;31m'
YELLOW='\033[1;33m'
GREEN='\033[0;32m'
NC='\033[0m'

echo -e "${YELLOW}⚠️  正在停止限位挡块资产登记系统...${NC}"
echo ""

docker compose down

echo ""
echo -e "${GREEN}✅ 服务已全部停止${NC}"
echo ""
echo "💡 如需同时删除数据卷，执行: docker compose down -v"
